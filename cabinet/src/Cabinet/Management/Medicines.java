package Cabinet.Management;

public class Medicines {
    public String Name;
    public int Quantity;
    public int Price;
    public int TimesPerDay;

    public Medicines(String Name, int Quantity, int Price, int TimesPerDay) {
        this.Name = Name;
        this.Quantity = Quantity;
        this.Price = Price;
        this.TimesPerDay = TimesPerDay;
    }

}
